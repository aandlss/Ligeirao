    public class GeradorLocalizacao
    {
        private Timer timer;
        private readonly HttpClient httpClient;
        private readonly string apiUrl;
        private readonly List<(decimal Latitude, decimal Longitude)> rota;
        private int indiceAtual;

        public GeradorLocalizacao(string apiUrl, List<(decimal Latitude, decimal Longitude)> rota)
        {
            this.apiUrl = apiUrl;
            this.rota = rota;
            httpClient = new HttpClient();
        }

        public void IniciarGeracao()
        {
            indiceAtual = 0;
            timer = new Timer(async _ => await EnviarLocalizacaoPeriodicamente(), null, TimeSpan.Zero, TimeSpan.FromSeconds(2));
        }

        private async Task EnviarLocalizacaoPeriodicamente()
        {
            try
            {
                var localizacao = rota[indiceAtual];

                indiceAtual = (indiceAtual + 1) % rota.Count;

                var localizacaoDTO = new LocalizacaoVeiculoDTO
                {
                    IdVeiculo = 1,
                    Latitude = localizacao.Latitude,
                    Longitude = localizacao.Longitude,
                    Horario = DateTime.Now
                };

                var jsonLocalizacao = JsonSerializer.Serialize(localizacaoDTO);

                var conteudo = new StringContent(jsonLocalizacao, Encoding.UTF8, "application/json");

                var resposta = await httpClient.PostAsync(apiUrl, conteudo);

                resposta.EnsureSuccessStatusCode();

                Console.WriteLine($"Localização enviada com sucesso: {localizacao.Latitude}, {localizacao.Longitude}");
            }
            catch (Exception ex)
            {
                Console.WriteLine($"Erro ao enviar localização: {ex.Message}");
            }
        }

        public void PararGeracao()
        {
            timer?.Dispose();
        }
    }

